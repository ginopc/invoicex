    private void myinit() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) comTipo.getModel();
        model.addElement("");
        model.addElement("Principale");
        model.addElement("Amministrazione");
        model.addElement("Vendite");
        model.addElement("Acquisti");
        model.addElement("Tecnico");
        
        try {
            List list = dbu.getList(Db.getConn(), "select tipo from clie_forn_contatti where tipo not in ('Principale', 'Amministrazione', 'Vendite', 'Acquisti', 'Tecnico') group by tipo order by tipo");
            for (Object o : list) {
                if (StringUtils.isNotBlank(cu.s(o))) {
                    model.addElement(o);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        Vector v = new Vector();
        v.add("id");
        dati.dbChiave = v;
        dati.dbChiaveAutoInc = true;
        dati.dbNomeTabella = "clie_forn_contatti";
        dati.messaggio_nuovo_manuale = true;
        dati.edit_in_mem = true;
    }

