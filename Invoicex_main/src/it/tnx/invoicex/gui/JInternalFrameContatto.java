    private void butConfermaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butConfermaActionPerformed
        try {
            //salvare i dati 
            dati.dbSave();
            JInternalFrameClientiFornitori parent = (JInternalFrameClientiFornitori) getParentFrame();
//            parent.caricaContatti();

            Integer id_row;
            if (dbStato.equals(tnxDbPanel.DB_INSERIMENTO)) {
                parent.contattiModel.addRow();
                id_row = parent.contattiModel.getRowCount()-1;
            } else {
                id_row = id_row_model;
            }
            DebugFastUtils.dump(dati.row);
            for (Object key : dati.row.keySet()) {
                System.out.println("key: " + key);
                parent.contattiModel.setValueAt(dati.row.get(key), id_row, parent.contattiModel.colonne.get(key));
            }
            
            parent.dati.dbForzaModificati();
            
        } catch (Exception e) {
            SwingUtils.showExceptionMessage(this, e);
        }
        
        try {
            setClosed(true);
        } catch (Exception e) {
            SwingUtils.showExceptionMessage(this, e);
        }
    }//GEN-LAST:event_butConfermaActionPerformed

