    private void aggiornaDaIban() {
        String i = iban.getText();
        
        String a = StringUtils.substring(i, 5, 10);
        abi.setText(a);
            
        String c = StringUtils.substring(i, 10, 15);
        cab.setText(c);
        
        String ccs = StringUtils.substring(i, 15, 27);
        cc.setText(ccs);
    }
    
    private void aggiornaAbi() {
        if (abi.getText().length() == 5) {
            try {
                lababi.setText(cu.s(dbu.getObject(Db.getConn(), "select nome from banche_abi where abi = " + dbu.sql(abi.getText()), false)));
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        } else {
            lababi.setText("");
        }
    }
    
    private void aggiornaCab() {
        if (cab.getText().length() == 5) {
            try {
                labcab.setText(cu.s(dbu.getObject(Db.getConn(),
                        "select CONCAT(IFNULL(com.comune,''), ' - ', IFNULL(cab.indirizzo,'')) as filiale "
                                + " from banche_cab cab "
                                + " left join comuni com on cab.codice_comune = com.codice \n"
                                + " where cab.abi = " + dbu.sql(abi.getText()) + " and cab.cab = " + dbu.sql(cab.getText())
                        , false)));
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        } else {
            labcab.setText("");
        }
    }

    
